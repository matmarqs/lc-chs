unsigned int mod(long long x) {
    return (x >= 0) ? (x % MOD) : (MOD - (-x) % MOD) % MOD;
}

void pointer_swap(int (**a)[SIZE][SIZE], int (**b)[SIZE][SIZE]) {
    int (*temp)[SIZE][SIZE] = *a;
    *a = *b;
    *b = temp;
}

// Matrix multiplication: C = A Ã— B (mod MOD)
void multiply(const ll A[SIZE][SIZE], const ll B[SIZE][SIZE], ll C[SIZE][SIZE]) {
    for (int i = 0; i < SIZE; ++i) {
        for (int k = 0; k < SIZE; ++k) {
            if (A[i][k] == 0) continue;
            for (int j = 0; j < SIZE; ++j) {
                C[i][j] = (C[i][j] + A[i][k] * B[k][j]) % MOD;
            }
        }
    }
}

